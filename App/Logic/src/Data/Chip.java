package Data;

public class Chip {

    String serialNumber;
    String action;
    public void setSerialNumber(String serialNumber){
        this.serialNumber =serialNumber;
    }
    public void setAction(String action){
        this.action= action;
    }
    public String getSerialNumber(){
        return this.serialNumber;
    }
    public String getAction(){
        return this.action;
    }
}
